# Operadores Matematicos 

"""

    - Soma: +
    - Subtração: -
    - Multiplicação: *
    - Divisão: /
    - Resto da Divisão: %
    - Potência: **
"""

numero1 = 10
numero2 = 20

print(numero1 + numero2)
print(numero1 - numero2)
print(numero1 * numero2)
print(numero1 / numero2)
print(numero1 // numero2)
print(20 // 3)
print(20 % 3) #resto da divissão
print (2 ** 3) #potencia 

